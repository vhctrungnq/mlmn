package vn.com.vhc.vmsc2.statistics.web.filter;

import java.util.Date;

public class HourReport {
	private Date day;
	private Integer hour;

	public Date getDay() {
		return day;
	}

	public void setDay(Date day) {
		this.day = day;
	}

	public Integer getHour() {
		return hour;
	}

	public void setHour(Integer hour) {
		this.hour = hour;
	}

}
