package vo.alarm.utils;

public class CableET4MasterFilter {
	private String area;
	private String exchange;
	private String snt;
	private String pos;
	private String ddf;
	private String info;
	
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getExchange() {
		return exchange;
	}
	public void setExchange(String exchange) {
		this.exchange = exchange;
	}
	public String getSnt() {
		return snt;
	}
	public void setSnt(String snt) {
		this.snt = snt;
	}
	public String getPos() {
		return pos;
	}
	public void setPos(String pos) {
		this.pos = pos;
	}
	public String getDdf() {
		return ddf;
	}
	public void setDdf(String ddf) {
		this.ddf = ddf;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}

}
